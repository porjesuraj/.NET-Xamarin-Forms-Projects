using Foundation;
using UIKit;
using SQLite;
using DeliveryApp.iOS.Models;

namespace DeliveryApp.iOS
{
    // The UIApplicationDelegate for the application. This class is responsible for launching the
    // User Interface of the application, as well as listening (and optionally responding) to application events from iOS.
    [Register("AppDelegate")]
    public class AppDelegate : UIResponder, IUIApplicationDelegate
    {

        public static  SQLiteAsyncConnection connect { get; set; }

        [Export("window")]
        public UIWindow Window { get; set; }

        [Export("application:didFinishLaunchingWithOptions:")]
        public bool FinishedLaunching(UIApplication application, NSDictionary launchOptions)
        {
            // Override point for customization after application launch.
            // If not required for your application you can safely delete this method

           // CurrentPlatform

            connect = new SQLiteAsyncConnection(Constants.Database.DbPath);

            if(connect != null)
            {
                connect.CreateTableAsync<User>();
                connect.CreateTableAsync<Delivery>();
                connect.CreateTableAsync<DeliveryPerson>();
                System.Console.WriteLine("Database Connection Establised");
            }else
            {
                System.Console.WriteLine("Database Connection not Establised");

            }

            return true;
        }

        // UISceneSession Lifecycle

